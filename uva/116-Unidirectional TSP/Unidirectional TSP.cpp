void getPath(int x, int y, int c, int l)
{
	if (y > c)
		return;

	cout << x << " ";

	if (ans[x][y] == 1)
		if (x - 1 == 0)
			getPath(l, y + 1, c, l);
		else
			getPath(x - 1, y + 1, c, l);
	if (ans[x][y] == 2)
		getPath(x, y + 1, c, l);
	if (ans[x][y] == 3)
		if (x + 1 > l)
			getPath(1, y + 1, c, l);
		else
			getPath(x + 1, y + 1, c, l);
}

void out(int l, int c)
{
	int tmp = INT_MAX, pos;

	for (int i = 1; i <= l; i++)
		if (tmp > dp[i][1])
		{
			tmp = dp[i][1];
			pos = i;
		}

	getPath(pos, 1, c, l);

	cout << endl << tmp << endl;
}

int main()
{
	int l, c;

	while (scanf("%d%d", &l, &c) == 2)
	{
		//init
		for (int i = 0; i <= l + 1; i++)
			for (int j = 0; j <= c + 1; j++)
				ans[i][j] = dp[i][j] = 0;

		for (int i = 1; i <= l; i++)
			for (int j = 1; j <= c; j++)
				cin >> vt[i][j];

		findLeast(l, c);
		out(l, c);
	}

	return 0;
}

